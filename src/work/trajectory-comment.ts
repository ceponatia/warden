import { TrajectoryStore } from "./trajectory-store.js";
import {
  generateProjectStateLens,
  generateLocalImpactLens,
} from "./trajectory-lenses.js";
import { renderTrajectoryComment } from "./trajectory-comment-renderer.js";
import { upsertTrajectoryComment } from "../github/comment.js";
import { fetchPrDetails, fetchPrDiff } from "../github/pr.js";
import { resolveAuthenticatedGithubLogin } from "../github/client.js";
import { loadRepoConfigs, getRepoConfigBySlug } from "../config/loader.js";
import type { TrajectoryGraph } from "../types/trajectory.js";

import { resolveProviderConfig } from "../agents/provider.js";

export async function postTrajectoryCommentOnPr(
  owner: string,
  repo: string,
  prNumber: number,
  repoSlug: string,
  options: {
    includeLocalImpact: boolean;
    triggeredBy?: "webhook" | "manual";
  },
): Promise<void> {
  const store = new TrajectoryStore(repoSlug);
  const graph = await store.load();
  const configs = await loadRepoConfigs();
  const repoConfig = getRepoConfigBySlug(configs, repoSlug);
  const aiEnabled = repoConfig.trajectory?.aiEnrichment === true;

  if (!aiEnabled) {
    const fallbackBody = renderTextOnlyTrajectoryComment(graph, prNumber);
    const expectedBotLogin = await resolveAuthenticatedGithubLogin();
    await upsertTrajectoryComment(owner, repo, prNumber, fallbackBody, {
      expectedBotLogin,
    });
    return;
  }

  const providerConfig = resolveProviderConfig();

  // 1. Generate Project State lens (always)
  const projectStateMmd = await generateProjectStateLens(graph, {
    triggeredBy: options.triggeredBy,
    prNumber,
  });

  // 2. Generate Local Impact lens (on merge only)
  let localImpactMmd: string | undefined;
  if (options.includeLocalImpact) {
    try {
      const [prDetails, prDiff] = await Promise.all([
        fetchPrDetails(owner, repo, prNumber),
        fetchPrDiff(owner, repo, prNumber),
      ]);
      localImpactMmd = await generateLocalImpactLens(
        graph,
        prDiff,
        prDetails.title,
        prDetails.body ?? "",
        {
          triggeredBy: options.triggeredBy,
          prNumber,
          denyPatterns: repoConfig.trajectory?.diffDenyPatterns,
        },
      );
    } catch (error) {
      console.error("Failed to generate Local Impact lens:", error);
    }
  }

  // 3. Render comment body
  const body = renderTrajectoryComment({
    projectState: projectStateMmd,
    localImpact: localImpactMmd,
    prNumber,
    repoSlug,
    aiModel: providerConfig.model,
  });

  // 4. Post/update comment
  const expectedBotLogin = await resolveAuthenticatedGithubLogin();
  await upsertTrajectoryComment(owner, repo, prNumber, body, {
    expectedBotLogin,
  });
}

function renderTextOnlyTrajectoryComment(
  graph: TrajectoryGraph,
  prNumber: number,
): string {
  const openCount = graph.nodes.filter(
    (node) => node.status === "opened",
  ).length;
  const closedCount = graph.nodes.filter(
    (node) => node.status === "closed",
  ).length;
  const blockedCount = graph.nodes.filter(
    (node) => node.status === "blocked",
  ).length;
  const recentOpenNodes = graph.nodes
    .filter((node) => node.status === "opened")
    .slice(0, 5);

  const parts: string[] = [];
  parts.push(`### Warden Trajectory Summary - ${graph.repoSlug}`);
  parts.push("");
  parts.push("| Status | Count |");
  parts.push("|--------|-------|");
  parts.push(`| Open | ${openCount} |`);
  parts.push(`| Closed | ${closedCount} |`);
  parts.push(`| Blocked | ${blockedCount} |`);

  if (recentOpenNodes.length > 0) {
    parts.push("");
    parts.push("#### Open Items");
    for (const node of recentOpenNodes) {
      const refs =
        node.findingRefs.length > 0
          ? ` - refs: ${node.findingRefs.join(", ")}`
          : "";
      parts.push(`- **${node.title}** (${node.type})${refs}`);
    }
  }

  parts.push("");
  parts.push(
    "AI trajectory visualization disabled. Enable via `trajectory.aiEnrichment` in repo config.",
  );
  parts.push(
    `\n---\n*Generated by [Warden](https://github.com/ceponatia/warden) for PR #${prNumber}*`,
  );
  return parts.join("\n");
}
