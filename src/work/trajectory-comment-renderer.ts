export interface TrajectoryCommentSections {
  projectState?: string;
  localImpact?: string;
  prNumber: number;
  repoSlug: string;
  aiModel?: string;
}

export function renderTrajectoryComment(sections: TrajectoryCommentSections): string {
  const parts: string[] = [];
  parts.push(`### 🛡️ Warden Trajectory — ${sections.repoSlug}`);

  if (sections.localImpact) {
    parts.push("#### 🎯 Local Impact (this PR)");
    parts.push("```mermaid");
    parts.push(sections.localImpact.trim());
    parts.push("```");
  }

  if (sections.projectState) {
    parts.push("<details>");
    parts.push("<summary><b>🔭 View Project State Snapshot</b></summary>");
    parts.push("\n");
    parts.push("```mermaid");
    parts.push(sections.projectState.trim());
    parts.push("```");
    parts.push("</details>");
  }

  const modelInfo = sections.aiModel ? ` utilizing **${sections.aiModel}**` : "";
  parts.push(`\n---\n*Generated by [Warden](https://github.com/ceponatia/warden)${modelInfo} for PR #${sections.prNumber}* <!-- warden-trajectory-comment -->`);
  return parts.join("\n\n");
}
