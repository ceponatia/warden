export interface TrajectoryCommentSections {
  projectState?: string;
  localImpact?: string;
  summary?: string;
  prNumber: number;
  repoSlug: string;
}

export function renderTrajectoryComment(sections: TrajectoryCommentSections): string {
  const parts: string[] = [];
  parts.push(`## Warden Trajectory — ${sections.repoSlug}`);

  if (sections.summary) {
    parts.push(sections.summary);
  }

  if (sections.projectState) {
    parts.push("### Project State");
    parts.push("```mermaid");
    parts.push(sections.projectState);
    parts.push("```");
  }

  if (sections.localImpact) {
    parts.push("### Local Impact (this PR)");
    parts.push("```mermaid");
    parts.push(sections.localImpact);
    parts.push("```");
  }

  parts.push(`\n---\n*Generated by [Warden](https://github.com/ceponatia/warden) for PR #${sections.prNumber}*`);
  return parts.join("\n\n");
}
